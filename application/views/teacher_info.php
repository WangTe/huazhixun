			<p>杨帆简介</p>
		</div>
		<p class="text_paragraph">十余年营销与管理实战经验，七年管理咨询与培训专业积累，对于中外企业进行具体实施和培训咨询有着丰富的实践经验多年咨询培训行业经验，面对中国企业、中国人，通过自身的实践，逐步形成了适宜于本土化管理的咨询培训风格。曾为多家中外优秀企业提供过咨询培训服务工作，其具有针对性的、实战性的咨询培训方案和针对企业管理的独到见解得到了众多企业的认可和赞誉。</p>
		<h2>擅长领域：</h2>
		<p class="text_paragraph">通用管理类 领导力与执行力 团队建设 班组建设</p>
		<h2>培训理念：</h2>
		<p class="text_paragraph">企业培训不是基于知道而培训，而是基于做到而培训，所以一个实效的培训不应只是理念的导入，而更应该注重实操工具的传授。</p>
		<p class="text_paragraph">杨帆老师在培训中除了传授知识之外，更注重改变学员的思维方式，因为如何想决定如何做，思维方式不改变，行为是不会改变的，只有学员的思维方式发生了变化，才能真正达成培训的目的：学以致变、学以致用。</p>
		<p class="text_paragraph">这个世界上最难做到的事情就是去改变一个人，其实人们不拒绝改变，而拒绝被改变，所有强加的外力都会引发学员的抵触与对抗，只有学员自己达成了心理认同，才会主动去变，杨老师擅长对学员思维进行潜移默化的引导，凭借对成人心理的深入研究，通过对培训的专业化操作以及教练技术、心理技术的应用，于无形中引导学员完成自我改变和自我升级。</p>
		<p class="text_paragraph">杨帆老师现具有多年企业管理、培训、及咨询经历，课程内容实战性极强，善于现场解决学员工作中的具体问题，其课程案例众多并以其诙谐、幽默的风格深受学员推崇。</p>
		<h2>曾讲服务的部分企业：</h2>	
		<p class="text_paragraph">中石化、中海油、中国移动、中国电信、中国联通、中钢、中船、汇丰行 、黄金集团、安徽中烟、辽宁红塔、中粮、中牧、金星啤酒、崂山矿泉水、蒙牛、汇源果汁、搜狐、中国招投标网、深圳海普瑞制药、徐州重工 、三一重工、斗山机械、中联重科、德尔福、通用汽车、现代汽车、神州租车、中国银行、工商银行、农业银行、浦发银行、广州地铁 、南京地铁、首都机场、博维航空设施、航天三院、国航、华北空管局、中国核电 、河北电力、上海华宝、四川金路、河南永煤、北京雅宝路等几百家企业。</p>
		<h2>授课风格：</h2>
		<p class="text_paragraph">演说家风采，脱口秀幽默，情景式案例。对问题一针见血，只言片语，改变很多人；</p>
		<p class="text_paragraph">用逆向思维导入培训模式，具有实战性，课程与众不同之处在于实战，拒绝空洞的理论和教条；</p>
		<p class="text_paragraph">实战+互动：培训深入浅出、案例丰富、互动性强见长、内容互动而非形式互动；</p>
		<p class="text_paragraph">学员反映：融理念、战略、技巧、案例为一体、生动、互动，学员爱听。</p>
